public class Calculator {
    public double addNums(double a, double b){
        return a + b;
    }

    public double subtractNums(double a, double b){
        return a - b;
    }

    public double multiplyNums(double a, double b){
        return a * b;
    }

    public double divideNums(double a, double b){
        return a / b;
    }
}
